"""
Канонические роли пользователей (этап 6, брейншторм ролей).

  ADMIN        — полный доступ: пользователи и роли, шаблоны, контрагенты
                 (включая удаление), импорт/экспорт, audit_log.
  DIRECTOR     — как MANAGER, плюс редактирование карточек контрагентов,
                 экспорт контрагентов в Excel и просмотр audit_log. Импорт —
                 НЕТ (загрузку данных внутрь делает только Admin, см.
                 брейншторм).
  TOP_MANAGER  — как MANAGER, плюс редактирование карточек контрагентов и
                 экспорт в Excel. В отличие от DIRECTOR — без просмотра
                 audit_log (запрошено явно: "менеджер, который может
                 редактировать контрагентов и экспортировать базу").
  TESTER       — ровно как TOP_MANAGER, плюс кнопка "Тестовые данные" в
                 форме генерации. Роль для проверки шаблонов: заполнить
                 форму демо-значениями в один клик и посмотреть, что
                 получилось. По правам доступа от TOP_MANAGER не отличается
                 НИЧЕМ — разница только в этой кнопке, и она чисто UX-ная
                 (см. canFillDemoData в permissions.js: серверной проверки
                 у неё нет и не нужно, демо-данные не привилегия).
  MANAGER      — рабочая роль: генерация документов, создание контрагентов.
                 Без РЕДАКТИРОВАНИЯ уже заведённых карточек, без удаления,
                 без импорта/экспорта, без шаблонов, без audit_log.

ROLES — единственный источник правды, как COUNTRIES/CONTRAGENT_TYPES в
app/tags.py: любая новая роль добавляется здесь и сразу используется во
всех проверках require_role(...) по всему коду.

Роль хранится в users.role как обычный varchar(16) — ограничения на уровне
БД нет, проверка только здесь (_validate_role в routers_auth.py сверяет с
ROLES). Поэтому новая роль НЕ требует миграции; следить надо лишь за тем,
чтобы её строковое значение влезло в 16 символов.
"""
ADMIN = "admin"
DIRECTOR = "director"
TOP_MANAGER = "top_manager"
TESTER = "tester"
MANAGER = "manager"

ROLES = (ADMIN, DIRECTOR, TOP_MANAGER, TESTER, MANAGER)

# Общие сокращения для Depends(require_role(...)) в роутерах — чтобы не
# перечислять одни и те же тройки/пары ролей в каждом файле по-разному.
ANY_ROLE = ROLES                              # любой залогиненный пользователь
# Экспорт контрагентов и просмотр audit_log раньше делили одну константу
# (CAN_EXPORT), пока обе роли с доступом к экспорту совпадали с ролями с
# доступом к audit_log. С появлением TOP_MANAGER (экспорт есть, audit_log
# нет) это перестало быть верным — разделены на две константы.
CAN_EXPORT_CONTRAGENTS = (ADMIN, DIRECTOR, TOP_MANAGER, TESTER)  # экспорт в Excel
CAN_VIEW_AUDIT_LOG = (ADMIN, DIRECTOR)                    # просмотр audit_log
CAN_VIEW_GENERATION_HISTORY = (ADMIN, DIRECTOR)           # вкладка "История генерации"
CAN_IMPORT = (ADMIN,)               # импорт контрагентов — только Admin
CAN_MANAGE_USERS = (ADMIN,)
CAN_MANAGE_TEMPLATES = (ADMIN,)
CAN_DELETE_CONTRAGENTS = (ADMIN,)

# Создание контрагента — рабочее действие, доступно всем ролям.
# Редактирование существующей карточки — НЕ для Manager: он заводит новых
# контрагентов и генерирует по ним документы, но правка уже заведённой
# карточки (в т.ч. reg_number — точного идентификатора) остаётся за
# Admin/Director/TopManager/Tester.
#
# Жили в routers_contragents.py до 17.07.2026 и переехали сюда: докстринг
# выше обещает, что новая роль добавляется В ОДНОМ месте, а на деле про эти
# две константы приходилось помнить отдельно — при добавлении TESTER на
# этом и споткнулись. Теперь обещание верное.
CAN_CREATE_CONTRAGENTS = (ADMIN, DIRECTOR, TOP_MANAGER, TESTER, MANAGER)
CAN_EDIT_CONTRAGENTS = (ADMIN, DIRECTOR, TOP_MANAGER, TESTER)
