"""
Канонические роли пользователей (этап 6, брейншторм ролей).

  ADMIN        — полный доступ: пользователи и роли, шаблоны, контрагенты
                 (включая удаление), импорт/экспорт, audit_log.
  DIRECTOR     — как MANAGER, плюс редактирование карточек контрагентов,
                 экспорт контрагентов в Excel и просмотр audit_log. Импорт —
                 НЕТ (загрузку данных внутрь делает только Admin, см.
                 брейншторм).
  TOP_MANAGER  — как MANAGER, плюс редактирование карточек контрагентов и
                 экспорт в Excel. В отличие от DIRECTOR — без просмотра
                 audit_log (запрошено явно: "менеджер, который может
                 редактировать контрагентов и экспортировать базу").
  MANAGER      — рабочая роль: генерация документов, создание контрагентов.
                 Без РЕДАКТИРОВАНИЯ уже заведённых карточек, без удаления,
                 без импорта/экспорта, без шаблонов, без audit_log.

ROLES — единственный источник правды, как COUNTRIES/CONTRAGENT_TYPES в
app/tags.py: любая новая роль добавляется здесь и сразу используется во
всех проверках require_role(...) по всему коду.
"""
ADMIN = "admin"
DIRECTOR = "director"
TOP_MANAGER = "top_manager"
MANAGER = "manager"

ROLES = (ADMIN, DIRECTOR, TOP_MANAGER, MANAGER)

# Общие сокращения для Depends(require_role(...)) в роутерах — чтобы не
# перечислять одни и те же тройки/пары ролей в каждом файле по-разному.
ANY_ROLE = ROLES                              # любой залогиненный пользователь
# Экспорт контрагентов и просмотр audit_log раньше делили одну константу
# (CAN_EXPORT), пока обе роли с доступом к экспорту совпадали с ролями с
# доступом к audit_log. С появлением TOP_MANAGER (экспорт есть, audit_log
# нет) это перестало быть верным — разделены на две константы.
CAN_EXPORT_CONTRAGENTS = (ADMIN, DIRECTOR, TOP_MANAGER)  # экспорт контрагентов в Excel
CAN_VIEW_AUDIT_LOG = (ADMIN, DIRECTOR)                    # просмотр audit_log
CAN_IMPORT = (ADMIN,)               # импорт контрагентов — только Admin
CAN_MANAGE_USERS = (ADMIN,)
CAN_MANAGE_TEMPLATES = (ADMIN,)
CAN_DELETE_CONTRAGENTS = (ADMIN,)
